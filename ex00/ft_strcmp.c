#include <stdio.h>

int	ft_strcmp(char *s1, char *s2)
{
	int	c;
	int	ret;

	c = 0;
	ret = 0;
	while ((s1[c] != '\0' || s2[c] != '\0') && (s1[c] == s2[c]))
	{
		c++;
	}
	ret = s1[c] - s2[c];
	return (ret);
}
